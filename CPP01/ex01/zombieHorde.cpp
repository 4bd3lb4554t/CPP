#include "Zombie.hpp"


Zombie* zombieHorde( int N, std::string name )
{
    if(N < 0)
    {
    std::cerr << "ERROR\n";
    return NULL;
    }
    Zombie *ObjZombie = new Zombie[N];

    for(int i = 0;i < N ; i++)
        ObjZombie[i].setName(name);

    return ObjZombie;
}